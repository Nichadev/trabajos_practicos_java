public class Recurso {
    private String muebles;
    private String dispositivoTecno;

    public Recurso(String muebles, String dispositivoTecno) {
        this.muebles = muebles;
        this.dispositivoTecno = dispositivoTecno;
    }

    public String getMuebles() {
        return muebles;
    }

    public void setMuebles(String muebles) {
        this.muebles = muebles;
    }

    public String getDispositivoTecno() {
        return dispositivoTecno;
    }

    public void setDispositivoTecno(String dispositivoTecno) {
        this.dispositivoTecno = dispositivoTecno;
    }
    
    
    
    
}
